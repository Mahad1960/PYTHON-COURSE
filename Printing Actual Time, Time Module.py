import time   # This is built-in module.
text=time.strftime("%H:%M:%S") # You can also use here '' these commas.
print("TIME:",text)
text=time.strftime('%H')
print("HOUR:",text)
text=time.strftime('%M')
print("MINUTES:",text)
text=time.strftime('%S')
print("SECONDS:",text)
text=time.strftime('%h')
print("MONTH:",text)
text=time.strftime('%m')
print("DATE:",text)